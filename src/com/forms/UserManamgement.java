          if (jComboBox1.getSelectedIndex()==-1) {
                    OptionHelper.showMessage("Please Select an Agent to block!!");
                    return;

                }

        String distributorId=getUserId(jComboBox1.getSelectedItem().toString());
        String reason=jTextArea1.getText();

        if(reason.trim().length()==0){
            OptionHelper.showMessage("Please enter the rejection remarks.");
            return;
        }


        String sql="update userinfo set ActiveFlag=2 where userid  ="+distributorId;
        int rows=ConnectionManager.executeUpdate(sql,null);
        

        if(rows>0){
            OptionHelper.showMessage("Agent has been blocked.");
            try{

                HashMap parameters=new HashMap();
                parameters.put("to", mailid);
                parameters.put("from", GetSetProperties.p("GMAIL_ID"));
                parameters.put("user", GetSetProperties.p("GMAIL_ID"));
                parameters.put("password", GetSetProperties.p("GMAIL_PASS"));
                parameters.put("subject","Your Login Id has been deactivated by distributor "+UserSession.loggedInUser.getFName()+" "+UserSession.loggedInUser.getLName());
                parameters.put("body", "Rejection Remarks are "+jTextArea1.getText());
                new MailUtility().sendEmail(parameters);
            }catch(Exception e){
e.printStackTrace();;
            }



        }else{
            OptionHelper.showError("Error while blocking the agent!!","Error");
            
        }


