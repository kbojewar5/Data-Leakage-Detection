  public String getUserId(String username) {
        String userId = "-1";
       
        for (int i = 0; i < UserManamgement.activeUserList.size(); i++) {
            UserModel object = (UserModel) UserManamgement.activeUserList.get(i);
            String uname = object.getFName() + " " + object.getLName().trim();

            if (uname.equalsIgnoreCase(username.trim())) {
                userId = object.getUserId();
                mailid=object.getEmailAddress();
                break;
            }
        }
        return userId;
    }
